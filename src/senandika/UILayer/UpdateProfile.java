    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {

            String username =
                    inputUsername.getText();

            String fullName =
                    inputFullName.getText();

            String gender =
                    cbGender
                    .getSelectedItem()
                    .toString();

            String stressText =
                    cbStress
                    .getSelectedItem()
                    .toString();

            int stressLevel =
                    convertStressLevel(stressText);

            String activity =
                    cbActivity
                    .getSelectedItem()
                    .toString();

            ProfileService service =
                    new ProfileService();

            String response =
                    service.updateProfile(
                            username,
                            fullName,
                            gender,
                            stressLevel,
                            activity
                    );

            JOptionPane.showMessageDialog(
                    this,
                    "Profile berhasil diperbarui"
            );
            
            Profile profil = new Profile();
            profil.setVisible(true);
            dispose();
            System.out.println(response);

        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage()
            );
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

