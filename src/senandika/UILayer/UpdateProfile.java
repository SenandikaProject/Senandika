    private int convertStressLevel(
        String level
    ) {

        switch (level) {

            case "Sangat Santai":
                return 1;

            case "Santai":
                return 2;

            case "Normal":
                return 3;

            case "Stres":
                return 4;

            case "Sangat Stres":
                return 5;

            default:
                return 3;
        }
    }
    
    private void loadProfile() {
      
        try {
            ProfileService service =
                    new ProfileService();

            String response =
                    service.getProfile();

            JsonObject json =
                    JsonParser
                    .parseString(response)
                    .getAsJsonObject();

            JsonObject data =
                    json.getAsJsonObject("data");

            inputUsername.setText(
                    data.get("username")
                    .getAsString()
            );

            inputFullName.setText(
                    data.get("full_name")
                    .getAsString()
            );

            cbGender.setSelectedItem(
                    data.get("gender")
                    .getAsString()
            );

            int stressLevel =
                    data.get("stress_level")
                    .getAsInt();

            switch (stressLevel) {

                case 1:
                    cbStress.setSelectedItem(
                            "Sangat Santai"
                    );
                    break;

                case 2:
                    cbStress.setSelectedItem(
                            "Santai"
                    );
                    break;

                case 3:
                    cbStress.setSelectedItem(
                            "Normal"
                    );
                    break;

                case 4:
                    cbStress.setSelectedItem(
                            "Stres"
                    );
                    break;

                case 5:
                    cbStress.setSelectedItem(
                            "Sangat Stres"
                    );
                    break;
            }

            cbActivity.setSelectedItem(
                    data.get("favorite_activity")
                    .getAsString()
            );

          }catch (Exception e) {

                JOptionPane.showMessageDialog(
                        this,
                        e.getMessage()
                );
            }
    }
    
